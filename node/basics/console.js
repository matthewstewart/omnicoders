// clear the console
console.clear();

// start a timer
console.time('Print Hello World To Console');

// print two new lines and a message to the console
console.log('\n\nHello World!');

// end timer and print result to console
console.timeEnd('Print Hello World To Console');

// print two new lines
console.log('\n\n');